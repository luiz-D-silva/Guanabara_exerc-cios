'''O símbolo % em Python é chamado de operador modulo. Ele retorna o resto da
divisão do operando da esquerda pelo operando da direita. Ele é usado para
obter o resto de um problema de divisão.'''

num = int(input('Digite um número qualquer: '))
if num % 2 == 0:
    print('O número digitado é PAR!')
else:
    print('O número digitado é IMPAR!')
